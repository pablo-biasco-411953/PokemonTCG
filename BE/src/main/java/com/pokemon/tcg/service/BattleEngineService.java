    private boolean esPokemonBasico(Card c) {
        if (c == null) return false;

        // 1. Verificar si el supertype es nulo
        if (c.getSupertype() == null) {
            System.out.println("вќЊ ERROR: La carta " + c.getNombre() + " no tiene supertype.");
            return false;
        }

        // 2. Verificar que sea PokГ©mon
        boolean esPokemon = "PokГ©mon".equalsIgnoreCase(c.getSupertype()) || "Pokemon".equalsIgnoreCase(c.getSupertype());

        // 3. Verificar que sea BГЎsico (Si la lista es nula, asumimos que no lo es)
        boolean esBasico = false;
        if (c.getSubtypes() != null) {
            for (String subtype : c.getSubtypes()) {
                if ("Basic".equalsIgnoreCase(subtype)) {
                    esBasico = true;
                    break;
                }
            }
        } else {
            System.out.println("вќЊ ERROR: La carta " + c.getNombre() + " no tiene la lista de subtipos.");
        }

        return esPokemon && esBasico;
    }

    private boolean esEnergia(Card c) {
        if (c == null || c.getSupertype() == null) return false;

        // рџљ© Si el supertype es Energy, no importa el tipo elemental (Fire, Water, etc)
        return c.getSupertype().equalsIgnoreCase("Energy");
    }
