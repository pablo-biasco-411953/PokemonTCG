@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly languages = LANGUAGES;
  readonly currentLanguage = signal<LanguageCode>(this.readInitialLanguage());

  constructor() {
    document.documentElement.lang = this.currentLanguage();
  }

  setLanguage(language: LanguageCode): void {
    this.currentLanguage.set(language);
    localStorage.setItem('pokefetch-language', language);
    document.documentElement.lang = language;
  }

  translate(key: string, params?: Record<string, any>): string {
    const language = this.currentLanguage();
    let text = TRANSLATIONS[language]?.[key] ?? TRANSLATIONS.en?.[key] ?? key;
    if (params) {
      Object.keys(params).forEach((pKey) => {
        const val = params[pKey] === undefined || params[pKey] === null ? '' : String(params[pKey]);
        text = text.replace(new RegExp(`{${pKey}}`, 'g'), val);
      });
    }
    return text;
  }

  private readInitialLanguage(): LanguageCode {
    const saved = localStorage.getItem('pokefetch-language') as LanguageCode | null;
    if (saved && LANGUAGES.some((lang) => lang.code === saved)) return saved;
    return 'en';
  }
}
